// –°–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ –±–æ—Ç–∞
function saveBot() {
    const index = document.getElementById('botIndex').value;
    const bot = {
        name: document.getElementById('botName').value,
        category: document.getElementById('botCategory').value,
        url: document.getElementById('botUrl').value,
        image: document.getElementById('botImage').value,
        description: document.getElementById('botDescription').value,
        showInfo: document.getElementById('botShowInfo').checked,
        inCarousel: document.getElementById('botInCarousel').checked
    };
    
    if (index === '') {
        // –î–æ–±–∞–≤–ª–µ–Ω–∏–µ –Ω–æ–≤–æ–≥–æ –±–æ—Ç–∞
        bot.id = config.bots.length > 0 ? Math.max(...config.bots.map(b => b.id)) + 1 : 1;
        config.bots.push(bot);
    } else {
        // –†–µ–¥–∞–∫—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ —Å—É—â–µ—Å—Ç–≤—É—é—â–µ–≥–æ
        bot.id = config.bots[index].id;
        config.bots[index] = bot;
    }
    
    saveConfig();
    renderBots();
    closeModal();
}

// –£–¥–∞–ª–µ–Ω–∏–µ –±–æ—Ç–∞
function deleteBot(index) {
    if (confirm('–í—ã —É–≤–µ—Ä–µ–Ω—ã, —á—Ç–æ —Ö–æ—Ç–∏—Ç–µ —É–¥–∞–ª–∏—Ç—å —ç—Ç–æ–≥–æ –±–æ—Ç–∞?')) {
        config.bots.splice(index, 1);
        saveConfig();
        renderBots();
    }
}

